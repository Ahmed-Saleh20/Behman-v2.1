            <h2>Psychological Disorders</h2>
            <img src="art5.jpg" class="img-test" alt="">
            <p class="paragraph p1" >
            The term psychological disorder is sometimes used to refer to what is more frequently known as mental disorders or psychiatric disorders. Mental disorders are patterns of behavioral or psychological symptoms that impact multiple areas of life. These disorders create distress for the person experiencing these symptoms.<br><br>
            While not a comprehensive list of every mental disorder, the following list includes some of the major categories of disorders described in the Diagnostic and Statistical Manual of Mental Disorders (DSM). The latest edition of the diagnostic manual is the DSM-5 and was released in May of 2013.1 The DSM is one of the most widely used systems for classifying mental disorders and provides standardized diagnostic criteria.<br>
            </p>
