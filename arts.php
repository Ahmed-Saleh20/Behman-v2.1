            <h3>Personality Disorders</h3>
            <p class="paragraph">
            Personality disorders are characterized by an enduring pattern of maladaptive thoughts, feelings, and behaviors that can cause serious detriments to relationships and other life areas 10.<br><br>
          Types of personality disorders include:<br>
            </p>
            <p>
           •	Antisocial personality disorder is characterized by a long-standing disregard for rules, social norms, and the rights of others.<br>
                People with this disorder typically begin displaying symptoms during childhood, have difficulty feeling empathy for others, and lack remorse for their destructive behaviors.<br>
            </p>
             <p>
            •	Avoidant personality disorder involves severe social inhibition and sensitivity to rejection.<br>
                 Such feelings of insecurity lead to significant problems with the individual's daily life and functioning.<br>
            </p>
             <p>
           •	Borderline personality disorder is associated with symptoms including emotional instability, unstable and intense interpersonal relationships, unstable self-image, and impulsive behaviors.<br>
            </p>
            <p>
           •	Dependent personality disorder involves a chronic pattern of fearing separation and an excessive need to be taken care of.<br>
                People with this disorder will often engage in behaviors that are designed to produce care-giving actions in others.<br>
            </p>
            <p>
          •	Histrionic personality disorder is associated with patterns of extreme emotionality and attention-seeking behaviors.<br>
                People with this condition feel uncomfortable in settings where they are not the center of attention, have rapidly changing emotions, and may engage in socially inappropriate behaviors designed to attract attention from others.<br>
            </p>
            <p>
          •	Narcissistic personality disorder is associated with a lasting pattern of exaggerated self-image, self-centeredness, and low empathy.<br>
                People with this condition tend to be more interested in themselves than with others.<br>
            </p>
            <p>
           •	Obsessive-compulsive personality disorder is a pervasive pattern of preoccupation with orderliness, perfectionism, inflexibility, and mental and interpersonal control.<br>
                This is a different condition than obsessive compulsive disorder (OCD).<br>
            </p>
            <p>
           •	Paranoid personality disorder is characterized by a distrust of others, even family, friends, and romantic partners.<br>
                People with this disorder perceive others intentions as malevolent, even without any evidence or justification.<br>
            </p>
            <p>
           •	Schizoid personality disorder involves symptoms that include being detached from social relationships.<br>
                People with this disorder are directed toward their inner lives and are often indifferent to relationships. They generally display a lack of emotional expression and can appear cold and aloof.<br>
            </p>
            <p>
           •	Schizotypal personality disorder features eccentricities in speech, behaviors, appearance, and thought.<br>
                People with this condition may experience odd beliefs or "magical thinking" and difficulty forming relationships.<br>
            </p>
