            <h2>Types of Psychological Disorders</h2>
            <p class="paragraph" >
            There are different types of Psychological Disorders.<br>
            </p>
        </div>
        <div class="blog">
            <h3>Neurodevelopmental Disorders</h3>
            
            <p class="paragraph">
            Neurodevelopmental disorders are those that are typically diagnosed during infancy, childhood, or adolescence. These psychological disorders include:<br>
            </p>
             <p>
            •	Intellectual disability (or Intellectual Developmental Disorder) was formerly referred to as mental retardation. This type of developmental disorder originates prior to the age of 18 and is characterized by limitations in both intellectual functioning and adaptive behaviors.<br>
            </p>
             <p>
            •	Limitations to intellectual functioning are often identified through the use of IQ tests, with an IQ score under 70 often indicating the presence of a limitation. Adaptive behaviors are those that involve practical, everyday skills such as self-care, social interaction, and living skills. <br>
            </p>
             <p>
            •	Global developmental delay is a diagnosis for developmental disabilities in children who are under the age of five. Such delays relate to cognition, social functioning, speech, language, and motor skills. It is generally seen as a temporary diagnosis applying to kids who are still too young to take standardized IQ tests. Once children reach the age where they are able to take a standardized intelligence test, they may be diagnosed with an intellectual disability.<br>
            </p>
            <p>
            •	Communication disorders are those that impact the ability to use, understand, or detect language and speech. The DSM-5 identifies four different subtypes of communication disorders: language disorder, speech sound disorder, childhood onset fluency disorder (stuttering), and social (pragmatic) communication disorder.<br>
            </p>
            <p>
            •	Autism spectrum disorder is characterized by persistent deficits in social interaction and communication in multiple life areas as well as restricted and repetitive patterns of behaviors. The DSM specifies that symptoms of autism spectrum disorder must be present during the early developmental period and that these symptoms must cause significant impairment in important areas of life including social and occupational functioning.<br>
            </p>
            <p>
            •	Attention-deficit hyperactivity disorder is characterized by a persistent pattern of hyperactivity-impulsivity and/or inattention that interferes with functioning and presents itself in two or more settings such as at home, work, school, and social situations. The DSM-5 specifies that several of the symptoms must have been present prior to the age of 12 and that these symptoms must have a negative impact on social, occupational, or academic functioning.<br>
            </p>
            <br>
        </div>
        <div class="blog">
            <h3>Bipolar and Related Disorders</h3>
            <p class="paragraph">
            Bipolar disorder is characterized by shifts in mood as well as changes in activity and energy levels. The disorder often involves experiencing shifts between elevated moods and periods of depression. Such elevated moods can be pronounced and are referred to either as mania or hypomania.<br>
            </p>
             <p>
            •	Mania is characterized by a distinct period of elevated, expansive, or irritable mood accompanied by increased activity and energy. Periods of mania are sometimes marked by feelings of distraction, irritability, and excessive confidence. People experiencing mania are also more prone to engage in activities that might have negative long-term consequences such as gambling and shopping sprees. <br>
            </p>
             <p>
            •	Depressive episodes are characterized by feelings of a depressed or sad mood along with a lack of interest in activities. It may also involve feelings of guilt, fatigue, and irritability. During a depressive period, people with bipolar disorder may lose interest in activities that they previously enjoyed, experience sleeping difficulties, and even have thoughts of suicide.<br>
            </p>
            <p class="paragraph">
            Both manic and depressive episodes can be frightening for both the person experiencing these symptoms as well as family, friends and other loved ones who observe these behaviors and mood shifts. Fortunately, appropriate and effective treatments, which often include both medications and psychotherapy, can help people with bipolar disorder successfully manage their symptoms.<br><br>
            Compared to the previous edition of the DSM, in the DSM-5 the criteria for manic and hypomanic episodes include an increased focus on changes in energy levels and activity as well as changes in mood.<br>
            </p>
            <br>
        </div>
        <div class="blog">
            <h3>Anxiety Disorders</h3>
            <p class="paragraph">
            Anxiety disorders are those that are characterized by excessive and persistent fear, worry, anxiety and related behavioral disturbances.5 Fear involves an emotional response to a threat, whether that threat is real or perceived. Anxiety involves the anticipation that a future threat may arise.<br><br>
            Types of anxiety disorders include:<br>
            </p>
             <p>
            •	Generalized anxiety disorder which is marked by excessive worry about everyday events. While some stress and worry are a normal and even common part of life, GAD involves worry that is so excessive that it interferes with a person's well-being and functioning. <br>
            </p>
             <p>
            •	Agoraphobia is characterized by a pronounced fear a wide range of public places. People who experience this disorder often fear that they will suffer a panic attack in a setting where escape might be difficult.<br>
            Because of this fear, those with agoraphobia often avoid situations that might trigger an anxiety attack. In some cases, this avoidance behavior can reach a point where the individual is unable to even leave their own home.<br>
            </p>
             <p>
            •	Social anxiety disorder is a fairly common psychological disorder that involves an irrational fear of being watched or judged. The anxiety caused by this disorder can have a major impact on an individual's life and make it difficult to function at school, work, and other social settings.<br>
            </p>
            <p>
            •	Specific phobias involve an extreme fear of a specific object or situation in the environment. Some examples of common specific phobias include the fear of spiders, fear of heights, or fear of snakes. The four main types of specific phobias involve natural events (thunder, lightening, tornadoes), medical (medical procedures, dental procedures, medical equipment), animals (dogs, snakes, bugs), and situational (small spaces, leaving home, driving). When confronted by a phobic object or situation, people may experience nausea, trembling, rapid heart rate, and even a fear of dying.<br>
            </p>
            <p>
            •	Panic disorder is a psychiatric disorder characterized by panic attacks that often seem to strike out of the blue and for no reason at all. Because of this, people with panic disorder often experience anxiety and preoccupation over the possibility of having another panic attack.<br>
            People may begin to avoid situations and settings where attacks have occurred in the past or where they might occur in the future. This can create significant impairments in many areas of everyday life and make it difficult to carry out normal routines.<br>
            </p>
            <p>
            •	Separation anxiety disorder is a type of anxiety disorder involving an excessive amount of fear or anxiety related to being separated from attachment figures. People are often familiar with the idea of separation anxiety as it relates to young children's fear of being apart from their parents, but older children and adults can experience it as well. When symptoms become so severe that they interfere with normal functioning, the individual may be diagnosed with separation anxiety disorder.<br>
            Symptoms involve an extreme fear of being away from the caregiver or attachment figure. The person suffering these symptoms may avoid moving away from home, going to school, or getting married in order to remain in close proximity to the attachment figure.<br>
            </p>
            <p class="paragraph">
            In one survey published in the Archives of General Psychiatry, it was estimated that as many as 18 percent of American adults suffer from at least one anxiety disorder.<br>
            </p>
            
            <br>
        </div>
        <div class="blog">
            <h3>Trauma and Stressor-Related Disorders</h3>
            <p class="paragraph">
            Trauma- and stressor-related disorders involve exposure to a stressful or traumatic event. These were previously grouped with anxiety disorders but are now considered a distinct category of disorders.<br><br>
            Disorders included in this category include:<br>
            </p>
            <p>
           •	Acute stress disorder, which is characterized by the emergence of severe anxiety for up to a one month period after exposure to a traumatic event such as natural disasters, war, accidents, and witnessing a death.<br>
            As a result, the individual may experience dissociative symptoms such as a sense of altered reality, an inability to remember important aspects of the event, and vivid flashbacks as if the event were reoccurring. Other symptoms can include reduced emotional responsiveness, distressing memories of the trauma, and difficulty experiencing positive emotions.<br>
            </p>
             <p>
            •	Adjustment disorders can occur as a response to a sudden change such as divorce, job loss, end of a close relationship, a move, or some other loss or disappointment. This type of psychological disorder can affect both children and adults and is characterized by symptoms such as anxiety, irritability, depressed mood, worry, anger, hopelessness, and feelings of isolation. <br>
            </p>
             <p>
            •	Post-traumatic stress disorder can develop after an individual has experienced exposure to actual or threatened death, serious injury, or sexual violence. Symptoms of PTSD include episodes of reliving or re-experiencing the event, avoiding things that remind the individual about the event, feeling on edge, and having negative thoughts. Nightmares, flashbacks, bursts of anger, difficulty concentrating, exaggerated startle response, and difficulty remembering aspects of the event are just a few possible symptoms that people with PTSD might experience.<br>
            </p>
             <p>
            •	Reactive attachment disorder can result when children do not form normal healthy relationships and attachments with adult caregivers during the first few years of childhood. Symptoms of the disorder include being withdrawn from adult caregivers and social and emotional disturbances that result from patterns of insufficient care and neglect.<br>
            </p>
            
            <br>
        </div>
        <div class="blog">
            <h3>Dissociative Disorders</h3>
            <p class="paragraph">
            Dissociative disorders are psychological disorders that involve a dissociation or interruption in aspects of consciousness, including identity and memory. <br><br>
            Dissociative disorders include:<br>
            </p>
            <p>
           •	Dissociative amnesia involves a temporary loss of memory as a result of dissociation. In many cases, this memory loss, which may last for just a brief period or for many years, is a result of some type of psychological trauma.<br>
           Dissociative amnesia is much more than simple forgetfulness.<br>
           Those who experience this disorder may remember some details about events but may have no recall of other details around a circumscribed period of time.<br>
            </p>
             <p>
            •	Dissociative identity disorder, formerly known as multiple personality disorder, involves the presence of two or more different identities or personalities. Each of these personalities has its own way of perceiving and interacting with the environment. People with this disorder experience changes in behavior, memory, perception, emotional response, and consciousness.<br>
            </p>
             <p>
            •	Depersonalization/derealization disorder is characterized by experiencing a sense of being outside of one's own body (depersonalization) and being disconnected from reality (derealization). <br>
                 People who have this disorder often feel a sense of unreality and an involuntary disconnect from their own memories, feelings, and consciousness.<br>
            </p>
            
            <br>
        </div>
        <div class="blog">
            <h3>Somatic Symptom and Related Disorders</h3>
            <p class="paragraph">
            Formerly referred to under the heading of somatoform disorders, this category is now known as somatic symptoms and related disorders. Somatic symptom disorders are a class of psychological disorders that involve prominent physical symptoms that may not have a diagnosable physical cause. <br><br>
            In contrast to previous ways of conceptualizing these disorders based on the absence of a medical explanation for the physical symptoms, the current diagnosis emphasizes the abnormal thoughts, feelings, and behaviors that occur in response to these symptoms.<br><br>
            Disorders included in this category:<br>
            </p>
            <p>
           •	Somatic symptom disorder involves a preoccupation with physical symptoms that make it difficult to function normally. This preoccupation with symptoms results in emotional distress and difficulty coping with daily life.<br>
           It is important to note that somatic symptoms do not indicate that the individual is faking his or her physical pain, fatigue, or other symptoms. In this situation, it is not so much the actual physical symptoms that are disrupting the individual's life as it is the extreme reaction and resulting behaviors.<br>
            </p>
             <p>
            •	Illness anxiety disorder is characterized by excessive concern about having an undiagnosed medical condition. Those who experience this psychological disorder worry excessively about body functions and sensations are convinced that they have or will get a serious disease, and are not reassured when medical tests come back negative.<br>
            This preoccupation with illness causes significant anxiety and distress. It also leads to changes in behavior such as seeking medical testing/treatments and avoiding situations that might pose a health risk.<br>
            </p>
             <p>
           •	Conversion disorder involves experiencing motor or sensory symptoms that lack a compatible neurological or medical explanation. In many cases, the disorder follows a real physical injury or stressful even which then results in a psychological and emotional response. <br>
            </p>
            <p>
           •	Factitious disorder, which used to have its own category, is now included under the somatic symptom and related disorders category of the DSM-5. A factitious disorder is when an individual intentionally creates, fakes, or exaggerates symptoms of illness. Munchausen syndrome, in which people feign an illness to attract attention, is one severe form of factitious disorder. <br>
            </p>
            
            <br>
        </div>
        <div class="blog">
            <h3>Feeding and Eating Disorders</h3>
            <p class="paragraph">
            Eating disorders are characterized by obsessive concerns with weight and disruptive eating patterns that negatively impact physical and mental health. Feeding and eating disorders that used to be diagnosed during infancy and childhood have been moved to this category in the DSM-5.8.<br><br>
           Types of eating disorders include:<br>
            </p>
            <p>
           •	Anorexia nervosa is characterized by restricted food consumption that leads to weight loss and a very low body weight. Those who experience this disorder also have a preoccupation and fear of gaining weight as well as a distorted view of their own appearance and behavior.<br>
            </p>
             <p>
            •	Bulimia nervosa involves binging and then taking extreme steps to compensate for these binges. These compensatory behaviors might include self-induced vomiting, the abuse of laxatives or diuretics, and excessive exercise. <br>
            </p>
             <p>
          •	Rumination disorder is marked by regurgitating previously chewed or swallowed food in order to either spit it out or re-swallow it. Most of those affected by this disorder are children or adults who also have a developmental delay or intellectual disability. Additional problems that can result from this behavior include dental decay, esophageal ulcers, and malnutrition.<br>
            </p>
            <p>
           •	Pica involves craving and consuming non-food substances such as dirt, paint, or soap. The disorder most commonly affects children and those with developmental disabilities. <br>
            </p>
            <p>
           •	Binge-eating disorder was first introduced in the DSM-5 and involves episodes of binge eating where the individual consumes an unusually large amount of over the course of a couple hours. Not only do people overeat, however, they also feel as if they have no control over their eating. <br>
                Binge eating episodes are sometimes triggered by certain emotions such as feeling happy or anxious, by boredom or following stressful events.<br>
            </p>
            
            <br>
        </div>
        <div class="blog">
            <h3>Sleep - Wake Disorders</h3>
            <p class="paragraph">
            Sleep disorders involve an interruption in sleep patterns that lead to distress and affects daytime functioning.<br><br>
           Examples of sleep disorders:<br>
            </p>
            <p>
           •	Narcolepsy is a condition in which people experience an irrepressible need to sleep. <br>
                People with narcolepsy may experience a sudden loss of muscle tone.<br>
            </p>
             <p>
           •	Insomnia disorder involves being unable to get enough sleep to feel rested. While all people experience sleeping difficulties and interruptions at some point, insomnia is considered a disorder when it is accompanied by significant distress or impairment over time.<br>
            </p>
             <p>
          •	Hypersomnolence disorder is characterized by excessive sleepiness despite an adequate main sleep period. People with this condition may fall asleep during the day at inappropriate times such as at work and school.<br>
            </p>
            <p>
           •	Breathing-related sleep disorders are those that involve breathing anomalies such as sleep apnea that can occur during sleep. These breathing problems can result in brief interruptions in sleep that can lead to other problems including insomnia and daytime sleepiness. <br>
            </p>
            <p>
          •	Parasomnias involve disorders that feature abnormal behaviors that take place during sleep. Such disorders include sleepwalking, sleep terrors, sleep talking, and sleep eating.<br>
            </p>
             <p>
          •	Restless legs syndrome is a neurological condition that involves having uncomfortable sensations in the legs and an irresistible urge to move the legs in order to relieve the sensations.<br>
            People with this condition may feel tugging, creeping, burning, and crawling sensations in their legs resulting in an excessive movement which then interferes with sleep.<br>
            </p>
            <p class="paragraph">
            Sleep disorders related to other mental disorders as well as sleep disorders related to general medical conditions have been removed from the DSM-5. The latest edition of the DSM also provides more emphasis on coexisting conditions for each of the sleep-wake disorders.<br><br>
          This change, the APA explains, "Underscores that the individual has a sleep disorder warranting independent clinical attention, in addition to any medical and mental disorders that are also present, and acknowledges the bidirectional and interactive effects between sleep disorders and coexisting medical and mental disorders".<br>
            </p>
            <br>
        </div>
        <div class="blog">
            <h3>Disruptive, Impulse-Control, and Conduct Disorders</h3>
            <p class="paragraph">
            Impulse-control disorders are those that involve an inability to control emotions and behaviors, resulting in harm to oneself or others.1 These problems with emotional and behavioral regulation are characterized by actions that violate the rights of others such as destroying property or physical aggression and/or those that conflict with societal norms, authority figures, and laws.<br><br>
           Types of impulse-control disorders:<br>
            </p>
            <p>
           •	Kleptomania involves an inability to control the impulse to steal.<br>
                People who have kleptomania will often steal things that they do not really need or that have no real monetary value.<br>
                Those with this condition experience escalating tension prior to committing a theft and feel relief and gratification afterwards.<br>
            </p>
             <p>
            •	Pyromania involves a fascination with fire that results in acts of fire-starting that endanger the self and others.<br>
            </p>
             <p>
          •	Intermittent explosive disorder is characterized by brief outbursts of anger and violence that are out of proportion for the situation.<br>
                 People with this disorder may erupt into angry outbursts or violent actions in response to everyday annoyances or disappointments.<br>
            </p>
            <p>
           •	Conduct disorder is a condition diagnosed in children and adolescents under the age of 18 who regularly violate social norms and the rights of others. <br>
                Children with this disorder display aggression toward people and animals, destroy property, steal and deceive, and violate other rules and laws. <br>
                These behaviors result in significant problems in a child's academic, work, or social functioning. <br>
            </p>
            <p>
           •	Oppositional defiant disorder begins prior to the age of 18 and is characterized by defiance, irritability, anger, aggression, and vindictiveness.<br>
                While all kids behave defiantly sometimes, kids with oppositional defiant disorder refuse to comply with adult requests almost all the time and engage in behaviors to deliberately annoy others.<br>
            </p>
            
            <br>
        </div>
        <div class="blog">
            <h3>Depressive Disorders</h3>
            <p class="paragraph">
            Depressive disorders are a type of mood disorder that include a number of conditions. They are all characterized by the presence of sad, empty, or irritable moods accompanied by physical and cognitive symptoms.<br><br>
           They differ in terms of duration, timing, or presumed etiology.<br>
            </p>
            <p>
           •	Disruptive mood dysregulation disorder: A childhood condition characterized by extreme anger and irritability. <br>
                Children display frequent and intense outbursts of temper.<br>
            </p>
             <p>
            •	Major depressive disorder: A condition characterized by loss of interest in activities and depressed mood which leads to significant impairments in how a person is able to function.<br>
            </p>
             <p>
          •	Persistent depressive disorder (dysthymia): This is a type of ongoing, chronic depression that is characterized by other symptoms of depression that, while often less severe, are longer lasting. <br>
                 Diagnosis requires experiencing depressed mood on most days for a period of at least two years.<br>
            </p>
            <p>
           •	Other or unspecified depressive disorder: This diagnosis is for cases when symptoms do not meet the criteria for the diagnosis of another depressive disorder, but they still create problems with an individual's life and functioning.<br>
            </p>
            <p>
           •	Premenstrual dysphoric disorder: This condition is a form of premenstrual syndrome (PMS) characterized by significant depression, irritability, and anxiety that begins a week or two before menstruation begins. Symptoms usually go away within a few day's following a woman's period.<br>
            </p>
            <p>
           •	Substance/medication-induced depressive disorder: This condition occurs when an individual experiences symptoms of a depressive disorder either while using alcohol or other substances or while going through withdrawal from a substance.<br>
            </p>
            <p>
           •	Depressive disorder due to another medical condition: This condition is diagnosed when a person's medical history suggests that their depressive symptoms may be the result of a medical condition.<br>
                Medical conditions that may contribute to or cause depression include diabetes, stroke, Parkinson's disease, autoimmune conditions, chronic pain conditions, cancer, infections and HIV/AIDS.<br>
            </p>
            <p class="paragraph">
            The depressive disorders are all characterized by feelings of sadness and low mood that are persistent and severe enough to affect how a person functions. Common symptoms shared by these disorders include difficulty feeling interested and motivated, lack of interest in previously enjoyed activities, sleep disturbances, and poor concentration.<br><br>
           The diagnostic criteria vary for each specific condition.<br><br>
            For major depressive disorder, diagnosis requires an individual to experience five or more of the following symptoms over the same two-week period.<br><br>
                One of these symptoms must include either depressed mood or loss of interest or pleasure in previously enjoyed activities.<br><br>
                Symptoms can include:<br>
            </p>
            <p>
           > Depressed mood for most or all of the day.<br>
            </p>
            <p>
            > Decreased or lack of interest in activities the individual previously enjoyed.<br>
            </p>
            <p>
           > Significant weight loss or gain, or decreased or increased appetite.<br>
            </p>
            <p>
            > Sleep disturbances (insomnia or hypersomnia).<br>
            </p>
            <p>
            > Feelings of slowed physical activity or restlessness.<br>
            </p>
            <p>
           	> Lack of energy or fatigue that lasts most or all of the day.<br>
            </p>
            <p>
            >	Feelings of guilt or worthlessness.<br>
            </p>
            <p>
            > Difficulty thinking or concentrating.<br>
            </p>
            <p>
            > Preoccupation with death or thoughts of suicide.<br>
            </p>
            <p class="paragraph">
            Treatments for depressive disorders often involve a combination of psychotherapy and medications.<br>
            </p>
            <br>
        </div>
        <div class="blog">
            <h3>Substance-Related and Addictive Disorders</h3>
            <p class="paragraph">
            Substance-related disorders are those that involve the use and abuse of different substances such as cocaine, methamphetamine, opiates, and alcohol. These disorders may include substance-induced conditions that can result in many associated diagnoses including intoxication, withdrawal, the emergence of psychosis, anxiety, and delirium.<br><br>
          Examples of substance-related disorders:<br>
            </p>
            <p>
           •	Alcohol-related disorders involve the consumption of alcohol, the most widely used (and frequently overused) drug in the United States.<br>
            </p>
             <p>
            •	Cannabis-related disorders include symptoms such as using more than originally intended, feeling unable to stop using the drug, and continuing to use despite adverse effects in one's life.<br>
            </p>
             <p>
            •	Inhalant-use disorder involves inhaling fumes from things such as paints or solvents. As with other substance-related disorders, people with this condition experience cravings for the substance and find it difficult to control or stop engaging in the behavior.<br>
            </p>
            <p>
           •	Stimulant use disorder involves the use of stimulants such as meth, amphetamines, and cocaine. <br>
            </p>
            <p>
           •	Tobacco use disorder is characterized by symptoms such as consuming more tobacco than intended, difficulty cutting back or quitting, cravings, and suffering adverse social consequences as a result of tobacco use.<br>
            </p>
            <p class="paragraph">
            The DSM-5 also includes gambling disorder under this classification. <br><br>
          The American Psychiatric Association explains that this change "reflects the increasing and consistent evidence that some behaviors, such as gambling, activate the brain reward system with effects similar to those of drugs of abuse and that gambling disorder symptoms resemble substance use disorders to a certain extent".<br>
            </p>
            <br>
        </div>
        <div class="blog">
            <h3>Neurocognitive Disorders</h3>
            <p class="paragraph">
            Neurocognitive disorders are characterized by acquired deficits in cognitive function. These disorders do not include those in which impaired cognition was present at birth or early in life.<br><br>
          Types of cognitive disorders include:<br>
            </p>
            <p>
           •	Delirium, also known as acute confusional state, that develops over a short period of time (usually a few hours or a few days) and is characterized by disturbances in attention and awareness.<br>
            </p>
             <p>
           •	Major and mild neurocognitive disorders have the primary feature of acquired cognitive decline in one or more areas including memory, attention, language, learning, and perception.<br>
                 These cognitive disorders can be due to medical conditions including Alzheimer's disease, HIV infection, Parkinson's disease, substance/medication use, vascular disease, and others.<br>
            </p>
             <br>
        </div>
        <div class="blog">
            <h3>Schizophrenia</h3>
            <p class="paragraph">
            Schizophrenia is a chronic psychiatric condition that affects a person’s thinking, feeling, and behavior. It is a complex, long-term condition that affects about one percent of people in the United States.<br><br>
          The DSM-5 diagnostic criteria specify that two or more symptoms of schizophrenia must be present for a period of at least one month. <br><br>
                One symptom must be one of the following: <br>
            </p>
            <p>
           •	Delusions: Beliefs that conflict with reality.<br>
            </p>
             <p>
            •	Hallucinations: Seeing or hearing things that aren't really there.<br>
            </p>
             <p>
           •	Disorganized speech: Words do not follow the rules of language and may be impossible to understand.<br>
            </p>
           <p class="paragraph">
            The second symptom may be one of the following: <br>
            </p>
            <p>
           •	Grossly disorganized or catatonic behavior (confused thinking, bizarre behavior or movements).<br>
            </p>
            <p>
           •	Negative symptoms (the inability to initiate plans, speak, express emotions, or feel pleasure).<br>
            </p>
            <p class="paragraph">
            Diagnosis also requires significant impairments in social or occupational functioning for a period of at least six months.  <br><br>
          The onset of schizophrenia is usually in the late teens or early 20s, with men usually showing symptoms earlier than women. Earlier signs of the condition that may occur before diagnosis include poor motivation, difficult relationships, and poor school performance.<br><br>
                The National Institute of Mental Health suggests that multiple factors may play a role in causing schizophrenia including genetics, brain chemistry, environmental factors, and substance use.<br><br>
                While there is no cure for schizophrenia, there are treatments available that make it possible to manage the symptoms of the condition. Treatments usually incorporate antipsychotic medications, psychotherapy, self-management, education, and social support.<br>
            </p>
            <br>
        </div>
         <div class="blog">
            <h3>Obsessive-Compulsive and Related Disorders</h3>
            <p class="paragraph">
            Obsessive-compulsive and related disorders is a category of psychiatric conditions that include:<br>
            </p>
            <p>
           •	Obsessive-compulsive disorder (OCD).<br>
            </p>
             <p>
           •	Body-dysmorphic disorder.<br>
            </p>
              <p>
           •	Hoarding disorder.<br>
            </p>
              <p>
           •	Trichotillomania (hair-pulling disorder).<br>
            </p>
              <p>
          •	Excoriation disorder (skin picking).<br>
            </p>
              <p>
           •	Substance/medication-induced obsessive-compulsive and related disorder.<br>
            </p>
              <p>
           •	Obsessive-compulsive and related disorder due to another medical condition.<br>
            </p>
             <p class="paragraph">
            Each condition in this classification has its own set of diagnostic criteria.<br>
            </p>
             <p>
           •    Obsessive-Compulsive Disorder.<br>
            </p>
             <p class="paragraph">
           The diagnostic criteria in the DSM-5 specify that in order to be diagnosed with obsessive-compulsive disorder, a person must experience obsessions, compulsions, or both.<br>
            </p>
             <p>
           >	Obsessions are defined as recurrent, persistent thoughts, impulses, and urges that lead to distress or anxiety.<br>
            </p>
             <p>
           >	Compulsions are repetitive and excessive behaviors that the individual feels that they must perform. These actions are performed to reduce anxiety or to prevent some dreaded outcome from occurring.<br>
            </p>
             <p class="paragraph">
          The obsessions and compulsions must also be time-consuming (taking up an hour or more per day) or cause significant distress or functional impairment, must not be attributable to another medical condition or substance use, and must not be better explained by another psychiatric condition (such as generalized anxiety disorder). <br>
            </p>
             <p class="paragraph">
           Treatments for OCD usually focus on a combination of therapy and medications. Cognitive-behavioral therapy (CBT) or a form of CBT known as exposure and response prevention (ERP) if commonly used.<br><br>
                 Antidepressants such as clomipramine or fluoxetine may also be prescribed to manage symptoms.<br>
            </p>
             
