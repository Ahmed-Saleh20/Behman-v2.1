            <h2>What is psychology?</h2>
            <p class="paragraph" >
            The mind is highly complex, and conditions that relate to it can be hard to treat.<br><br>
Thought processes, emotions, memories, dreams, perceptions, and so on cannot be seen physically, like a skin rash or heart defect.<br><br>
While physical signs of some mental health issues can be observed, such as the plaques that develop with Alzheimer’s disease, many theories of psychology are based on observation of human behavior.<br><br>
A practicing psychologist will meet with patients, carry out assessments to find out what their concerns are and what is causing any difficulties, and recommend or provide treatment, for example, through counselling and psychotherapy.<br><br>
Psychologists may have other roles, too. They may carry out studies to advise health authorities and other bodies on social and other strategies, assess children who find it difficult to learn in school, give workshops on how to prevent bullying, work with recruitment teams in companies, and much more.<br>
            </p>
